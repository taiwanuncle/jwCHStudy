def add_korean_meanings():
    with open(DATA_PATH, "r", encoding="utf-8") as f:
        data = json.load(f)

    mapped = 0
    for idiom in data["idioms"]:
        word = idiom["idiom"]
        if word in KOREAN_MEANINGS:
            idiom["meaning_ko"] = KOREAN_MEANINGS[word]
            mapped += 1
        elif not idiom["meaning_ko"]:
            # 중국어 설명이 짧으면 그대로 사용
            zh = idiom["meaning_zh"]
            if len(zh) <= 20:
                idiom["meaning_ko"] = zh
            else:
                # 첫 번째 문장(。까지)만 사용
                first_sentence = zh.split("。")[0]
                if len(first_sentence) <= 30:
                    idiom["meaning_ko"] = first_sentence
                else:
                    idiom["meaning_ko"] = first_sentence[:30] + "…"

    with open(DATA_PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Korean meanings mapped: {mapped}/{len(data['idioms'])}")
    print(f"Total idioms: {len(data['idioms'])}")


if __name__ == "__main__":
    add_korean_meanings()
