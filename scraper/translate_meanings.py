def main():
    with open("web/data/idioms.json", "r", encoding="utf-8") as f:
        data = json.load(f)

    updated = 0
    still_chinese = 0
    for idiom in data["idioms"]:
        ko = idiom.get("meaning_ko", "")
        has_chinese = any("\u4e00" <= c <= "\u9fff" for c in ko)
        if has_chinese:
            key = idiom["idiom"]
            if key in TRANSLATIONS:
                idiom["meaning_ko"] = TRANSLATIONS[key]
                updated += 1
            else:
                still_chinese += 1

    with open("web/data/idioms.json", "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Updated: {updated}")
    print(f"Still Chinese: {still_chinese}")


if __name__ == "__main__":
    main()
